// 사칙연산 : +, -, /, *, %, **

console.log(3 + 5); // 8
console.log(3 - 5); // -2
console.log(3 * 5); // 15
console.log(3 / 5); // 0.6
console.log(3 % 5); // 3 모듈러 연산. 나머지 연산자
console.log(3 ** 2); // 9

let a = 100;
console.log(a % 2);; //a 가 짝수, 홀수?

// + (접합연산자)
// 문자로 시작하면 뒤에 나오는 +는 접합연산자로 실행됨
console.log(10 + 20);
console.log("sum : " + 10 + 20);
console.log("sum : " + (10 + 20));
console.log(`sum : ${10 + 20}`);
